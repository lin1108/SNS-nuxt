                        <p>
                            　　再说若冰和十七，伏身在东镇庙西驿馆屋顶，听尚书府大管家求石抑扬顿挫地朗诵《获鹿记》，正沉浸在一派安静祥和的气氛中，也忘了什么月桂不月桂，剑胆不剑胆，忽听一声响亮，两个黑衣人飞身而入，挟起梅尚书就走。院子里顿时大乱。若冰这才想起自己手中还有一把龙泉剑，探起半身，警惕地观察着四周。见庙中的尚书府护卫和县衙士兵倾巢而出，紧追不舍。无奈两个黑衣人身形极为迅捷，来去如飞，架着一个人，丝毫不觉得累赘，箭一般朝着凤凰岭方向去了。</p>
                        <p>　　眼看着馆舍为之一空，只剩下几个无缚鸡之力不知所措的地方官员。若冰这才爬起身，悄声对十七说：</p>
                        <p>　　“走，我们去看看……”</p>
                        <p>
                            　　若冰从小就随大夫来过几次沂山，对沂山地理并不陌生，赶到凤凰岭下，见县衙的士兵已经散开，正在四下里搜山，不觉哑然失笑，心知依那两个黑衣人的速度，此刻只怕早已经翻到山那边，消失在峡谷中了，怎么可能还藏在这丛林里，让你们找呢？便也拎了拎手中宝剑，带十七快步上山。刚走到山半，猛听后面传来一句不怒自威的喝声：</p>
                        <p>　　 前面两位姑娘，请留步！”</p>
                        <p>　　若冰心里就是一跳，果然是那长风侠健步向她们走来。他身边另有一个秀才模样的袖笛男子，想必正是那穆陵关守将，笛声将军了。</p>
                        <p>　　长风侠来到跟前，狐疑地打量了若冰一眼，说：“姑娘们这是要去哪儿，看这身装扮，不象是山里人吧？”</p>
                        <p>
                            　　“我俩不象是山里人，二位官爷倒象山里人了？”因为参与了天溪渊行刺，若冰怕被他看出形迹，故意调皮地反问了一句，“这位是我家一个远房亲戚，听说这岭上有个奇观，叫做九龙汇汶，所以带她来看看景儿。”</p>
                        <p>　　“喔，既然知道到凤凰岭上来看九龙汇汶，那就说明你是当地人了。”笛声上前一步说，“很多人不明白其中关窍，知道此地叫做九龙口，那龙在哪儿？岂不知只有在这山半腰才能看到。”</p>
                        <p>　　说着，他返身一指。若冰等人顺着他的视线望去，果然见汶水蜿蜒，南边九条山脉直插河边，就象有九条龙在饮水一般。</p>
                        <p>　　“哇，久闻东镇庙是一块风水宝地，现在方知此言非虚！”长风侠惊叹一声。</p>
                        <p>　　“长风侠这是第一次随梅尚书来沂山吧？”笛声说，“梅尚书当年曾隐居沂山读书，熟知沂山地理风物。等日子久了，慢慢你也就知道了。”</p>
                        <p>　　“对，我们赶紧去找尚书大人！”长风侠猛击一掌。</p>
                        <p>　　“梅尚书也是一个老沂山，在故乡怎么会有仇人？但愿他平安无事……”</p>
                        <p>　　笛声说着，和长风侠分头去了。</p>
                        <p>　　若冰和十七忙转身就走，忽听草丛里嘻嘻一声，从路边冒出个小雨点来，蹦到二人身前，笑嘻嘻地说：</p>
                        <p>　　“这些个官兵有什么用？我就躲在树林子里，他们愣没瞧见！”</p>
                        <p>　　“啊，小雨姐姐，你怎么又回来了？”若冰吓了一跳，拿眼一瞅，却先看到了她手中另外拎着一把宝剑。</p>
                        <p>　　“这，这……”眼看着梦想就要成为现实，若冰都不敢相信会是真的。</p>
                        <p>　　“看见没？上古圣水宝剑！”</p>
                        <p>　　小雨点手一扬，就要拔剑出鞘，若冰连忙制止住她：</p>
                        <p>　　“我们快走，趁早离了这是非之地……”她拽了一把十七，三个人急急向岭下走去。</p>
                        <p>2</p>
                        <p>
                            　　三个人一起赶往望海镇，在路上小雨点说知，爹爹在法云寺禅修，天天早上在庙后的圣水泉边打太极拳。圣水泉系汶水之源，泉眼清幽，泉水甘冽，本来也没什么，近日来常常有祥光闪现。爹爹让寺人往深处一淘，竟淘得了一把上古的宝剑，便命名为圣水剑。</p>
                        <p>　　“啧啧，你看你爹爹多好啊！觅得了宝剑，先想到自己的宝贝女儿。哪象我，跟剑胆哥哥练剑，也要偷偷摸摸……”</p>
                        <p>　　若冰羡慕地说，又想到龙泉剑从此归自己所有，也是不胜之喜。十七则记挂着那花神，三个人说说笑笑，没废多少工夫便赶回了这个离白浪滩不远的小镇。</p>
                        <p>
                            　　原来这望海镇，全称望海台，又名禹王台，一说是大禹治水歇脚的地方，一说系秦始皇远望东海之处。更广泛的看法，倾向于远古城池年代久远，形成的一座废墟。不过都说当年在此台上，可以眺望东海，所以以望海镇名之。白浪河那时离大海究竟有多远不知道，但西行不远的沂山，自古就有“大海东来第一山”之说，沂山主峰玉皇顶卧石，都叫做探海石，所以当地居民多热衷于望海，在高台之上凭吊古迹，遥感一下海洋的气息，也在情理之中。</p>
                        <p>
                            　　十七跟着若冰和小雨点走进镇子，才知道这儿之所以以台名之，是因为镇中心的确有一处高台。高台上飞檐斗拱，象有几座庙宇。周边住家和店铺比邻而居，层层环绕。街道从中间向外延伸，纵横交错，跟八卦阵似的。那暖春坞则在镇子东南角，西边有一家锦绣房，另一边是家商号，叫做青天顺。</p>
                        <p>
                            　　三个人来到镇子中心，已是正午。知道暖春坞这种地方，都是赶夜场的，小雨点便带十七和若冰先找地儿吃饭。饭后，陪她俩在街上慢慢闲逛，看小镇的风味小吃，各式特产。说起这望海台，还是小雨来得多一些，又长她们几岁，给她俩一一做些介绍。若冰总是出来得少，这个也好奇，那个也好奇，东问西问的。十七则老是一副心不在焉的样子，时不时瞟中央那高台一眼。若冰问她怎么了，十七这才说：</p>
                        <p>　　“那台子上都有什么呀？”</p>
                        <p>　　“咦，我还真没上去过呢！小雨姐姐，那台上有什么，是大庙吗？”若冰快人快语。</p>
                        <p>　　小雨点说：“那台上有三个庙，中间禹王庙，一边是狐仙庙，一边是土地庙。”</p>
                        <p>　　“为什么有禹王庙？”若冰纳闷地问。</p>
                        <p>　　“大禹治水有大功呀，”小雨点说，“所以此地又叫做禹王台。”</p>
                        <p>　　十七听了，就觉心里咯噔一下。</p>
                        <p>　　三个人转了一圈，眼看着太阳西斜，才来到暖春坞前。临进门，小雨点略有点犹豫，说：“我还是失了计较。”</p>
                        <p>　　若冰问怎么了。小雨点说：“我们仨应该先去镇北面成衣店，花几个零碎银子，每人换一身衣裳。”</p>
                        <p>　　“这是为什么？”十七也好生奇怪。</p>
                        <p>　　“女扮男装呀，”小雨笑了笑说，“毕竟这种地方，就不是我们女孩儿家能进的……”</p>
                        <p>　　“瞧，那个女孩！不是先进去了吗？”若冰眼尖，指了指说。</p>
                        <p>　　十七和小雨抬头一看，有个衣着光鲜的妙龄女郎，挎着一个年轻人的臂膀，迈进了暖春坞。</p>
                        <p>
                            　　这望海台少年少女一起逛街的并不多见，何况还这样勾肩搭背。十七忍不住多看了几眼。小雨点说：“这两人我是认得的，一个是锦绣房的绣娘，叫做枫叶。那少年则是东边青天顺掌柜，人称顺公子。”</p>
                        <p>　　“那好。”若冰顿觉如释重负，“我们进去吧。”</p>
